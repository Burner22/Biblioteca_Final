    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed

        try {
            int codigoEjemplar = Integer.parseInt(jtfCodigoEjemplar.getText());
            Ejemplar ejemplar = ed.buscarEjemplar(codigoEjemplar);
            if (codigoEjemplar > 0) {
                if (ejemplar != null) {
                    jtfNombreLibro.setText(ejemplar.getLibro().getNombre());
                    jtfIsbnLibro.setText(String.valueOf(ejemplar.getLibro().getISBN()));
                    jtfAutorLibro.setText(ejemplar.getLibro().getAutor().getNombreAutor() + " " + ejemplar.getLibro().getAutor().getApellidoAutor());
                    jtfEstadoActualEjemplar.setText(ed.estadoEjemplar(codigoEjemplar));

                    jtfCodigoEjemplar.setEnabled(false);
                    jbBuscar.setEnabled(false);
                    jcbEstadosEjemplar.setEnabled(true);
                    jbActualizarEjemplar.setEnabled(true);
                    jbLimpiar.setEnabled(true);
                } else {
                    JOptionPane.showMessageDialog(this, "No hay un ejemplar con ese codigo!");
                    jcbEstadosEjemplar.setSelectedIndex(-1);
                    jcbEstadosEjemplar.setEnabled(false);
                    jbActualizarEjemplar.setEnabled(false);
                    jbLimpiar.setEnabled(false);
                    jtfCodigoEjemplar.requestFocus();

                }
            } else {
                JOptionPane.showMessageDialog(this, "Ingrese un codigo de ejemplar valido!");
                limpiarTextFields();
                jcbEstadosEjemplar.setSelectedIndex(-1);
                jcbEstadosEjemplar.setEnabled(false);
                jbActualizarEjemplar.setEnabled(false);
                jbLimpiar.setEnabled(false);
                jtfCodigoEjemplar.requestFocus();
            }

        } catch (NullPointerException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Se necesita un codigo de ejemplar valido!");
            jcbEstadosEjemplar.setSelectedIndex(-1);
            jcbEstadosEjemplar.setEnabled(false);
            jtfCodigoEjemplar.setText("");
            jtfCodigoEjemplar.requestFocus();
        }

    }//GEN-LAST:event_jbBuscarActionPerformed

