    private void jbActualizarEjemplarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbActualizarEjemplarActionPerformed
        int codigoEjemplar = Integer.parseInt(jtfCodigoEjemplar.getText());

        if (jcbEstadosEjemplar.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un estado para actualizar al lector!");
            jcbEstadosEjemplar.requestFocus();
        } else {
            int confirmarActualizacion = JOptionPane.showConfirmDialog(this, "Se esta por actualizar el estado del Ejemplar ¿Está seguro?");

            if (JOptionPane.OK_OPTION == confirmarActualizacion) {
                Ejemplar ejemplar = ed.buscarEjemplar(codigoEjemplar);
                String nuevoEstado = (String) jcbEstadosEjemplar.getSelectedItem();
                ed.actualizarEstado(ejemplar, nuevoEstado);
                JOptionPane.showMessageDialog(this, "Se actualizo el estado del ejemplar!");
                
                limpiarTextFields();
                jtfCodigoEjemplar.setEnabled(true);
                jbBuscar.setEnabled(true);
                jbActualizarEjemplar.setEnabled(false);
                jbLimpiar.setEnabled(false);
                jcbEstadosEjemplar.setSelectedIndex(-1);
                jcbEstadosEjemplar.setEnabled(false);
                jtfCodigoEjemplar.requestFocus();
            }else{
                JOptionPane.showMessageDialog(this, "Se cancelo la actualizacion del estado del ejemplar");
            }
        }
    }//GEN-LAST:event_jbActualizarEjemplarActionPerformed

    public void limpiarTextFields() {
        jtfCodigoEjemplar.setText("");
        jtfNombreLibro.setText("");
        jtfIsbnLibro.setText("");
        jtfAutorLibro.setText("");
        jtfEstadoActualEjemplar.setText("");
    }

