    public void armarCabecera(){
        ArrayList<Object> columnas= new ArrayList<>();
        columnas.add("Autor");
        columnas.add("ISBN");
        columnas.add("A침o publicaci칩n");
        columnas.add("Nombre");
        columnas.add("Editorial");
        columnas.add("Tipo");
        
        for(Object it: columnas){
            modelo.addColumn(it);
        }
        jtLibros.setModel(modelo);
    }
    public void llenarTodosLibros(){
        for(Libro l: ld.getAll()){
            modelo.addRow(new Object[]{l.getAutor(),l.getISBN(),l.getA침o(),l.getNombre(),l.getEditorial(),l.getTipo()});
        }
    }
    public void llenarBuscado(String nombre){
        List<Libro> l=ld.buscarLibro(nombre);
        for(Libro a:l){
             modelo.addRow(new Object[]{a.getAutor(),a.getISBN(),a.getA침o(),a.getNombre(),a.getEditorial(),a.getTipo()});
        }
       
    }
    public void borrarFilas(){
        int a=modelo.getRowCount()-1;
        
        for(int i=a;i>=0;i--){
            modelo.removeRow(i);
        }
        
    }
