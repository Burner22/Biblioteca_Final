    private void jbBuscarLectorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarLectorActionPerformed
        Lector lector=new Lector();
        
        try{
            int id=Integer.parseInt(jtIdLector.getText());
            lector=leda.buscarLector(id);
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Error en id");
        }
        
        
        jtNombreLector.setText(lector.getNombreLector());
        jtApellidoLector.setText(lector.getApellidoLector());
        jtDniLector.setText(String.valueOf(lector.getDniLector()));
        jcbEstadoLector.setSelected(lector.getEstado_lector());
        if(lector.getEstado_lector()){
            habilitarCampos();
            
        }else{
            anularCampos();
        }
    }//GEN-LAST:event_jbBuscarLectorActionPerformed

