    private void jbBuscarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarLibroActionPerformed
        String nombreLibro=jtNombreLibro.getText();
        if(nombreLibro!=null){
            limpiarTabla();
            llenarConLibros();
            jbRegistrar.setEnabled(true);
        }
    }//GEN-LAST:event_jbBuscarLibroActionPerformed

    private void jbRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRegistrarActionPerformed
        int fe=jtLibros.getSelectedRow();
        if(fe!=-1){
            
            Prestamo pres=new Prestamo();
            Ejemplar ejemplar=ejeda.BuscarEjemplarID((Integer)modelo.getValueAt(fe, 0));
            Lector lector= new Lector();
            try{
            int id=Integer.parseInt(jtIdLector.getText());
            lector=leda.buscarLector(id);
            pres.setEjemplar(ejemplar);
            pres.setLector(lector);
            pres.setEstado(true);
            LocalDate fecha=jdcFechaPrestamo.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            pres.setFecha_prestamo(fecha);
            
            prestamo.registrarPrestamo(pres);
            
            limpiarTabla();
        jtNombreLector.setText("");
        jtApellidoLector.setText("");
        jtDniLector.setText("");
        jtNombreLibro.setText("");
        jdcFechaPrestamo.cleanup();
        anularCampos();
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Error en id");
        }
            
        }else{
            JOptionPane.showMessageDialog(this, "No se selecciono una columna");
        }
    }//GEN-LAST:event_jbRegistrarActionPerformed

