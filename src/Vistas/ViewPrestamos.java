    public void armarCabecera(){
        ArrayList<Object> columnas= new ArrayList<>();
        columnas.add("idEjemplar");
        columnas.add("Nombre Libro");
        columnas.add("Año");
        columnas.add("Autor");
        columnas.add("Estado");
        
        
        for(Object it: columnas){
            modelo.addColumn(it);
        }
        jtLibros.setModel(modelo);
        
    }
    public void llenarConLibros(){
        List<Ejemplar> list=ejeda.buscarEjemplar(jtNombreLibro.getText());
        for(Ejemplar e:list){
            modelo.addRow(new Object[]{e.getId_ejemplar(),e.getLibro().getNombre(),e.getLibro().getAño(),e.getLibro().getAutor(),e.getEstado()});
        }
    }
    public void limpiarTabla(){
        int a=modelo.getRowCount()-1;
        
        for(int i=a;i>=0;i--){
            modelo.removeRow(i);
        }
    }
    

