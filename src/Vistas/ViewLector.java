    private void jbGuardarLectorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarLectorActionPerformed

        try {
            int dniLector = Integer.parseInt(jtfDni.getText());

            String nombreLector = jtfNombre.getText();
            String apellidoLector = jtfApellido.getText();
            String dirLector = jtfDireccion.getText();
            boolean estadoLector = true;

            if (nombreLector.isEmpty() || apellidoLector.isEmpty() || dirLector.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Para poder cargar a un lector se necesitan todos los datos!");
            } else if (tieneNum(nombreLector)) {
                JOptionPane.showMessageDialog(this, "Colocar solo letras en el nombre del lector!");
                jtfNombre.requestFocus();
            } else if (tieneNum(apellidoLector)) {
                JOptionPane.showMessageDialog(this, "Colocar solo letras en el apellido del lector!");
                jtfApellido.requestFocus();
            } else {
                Lector lector = new Lector(dniLector, nombreLector, apellidoLector, dirLector, estadoLector);
                ld.agregarLector(lector);
                limpiarTextFields();
                jbGuardarLector.setEnabled(false);
            }
        } catch (NumberFormatException | NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Para dar de alta a un lector se necesitan todos los datos y que sean validos");
        }
    }//GEN-LAST:event_jbGuardarLectorActionPerformed

    private void jtfDniFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtfDniFocusLost
        String nombreLector = jtfNombre.getText();
        String apellidoLector = jtfApellido.getText();

        if(nombreLector.length() > 0 && apellidoLector.length() > 0 && jtfDni.getText().length() > 0){
            jbGuardarLector.setEnabled(true);
        }
    }//GEN-LAST:event_jtfDniFocusLost

    public void limpiarTextFields() {
        jtfNombre.setText("");
        jtfApellido.setText("");
        jtfDni.setText("");
        jtfDireccion.setText("");
    }

    public boolean tieneNum(String str) {
        char aux[];

        aux = str.toCharArray();

        for (char c : aux) {
            if (Character.isDigit(c)) {
                return true;
            }
        }

        return false;
    }

