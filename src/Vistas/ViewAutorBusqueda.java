    public void armarCabecera(){
        ArrayList<Object> columnas= new ArrayList<>();
        columnas.add("ID");
        columnas.add("Nombre");
        columnas.add("Apellido");
        columnas.add("DNI");
        columnas.add("Fecha de Nacimiento");
        columnas.add("Nacionalidad");
        
        for(Object it: columnas){
            modelo.addColumn(it);
        }
        jtAutores.setModel(modelo);
    }
    public void llenarTodosAutores(){
        for(Autor a: ad.getAll()){
            modelo.addRow(new Object[]{a.getId_autor(),a.getNombreAutor(),a.getApellidoAutor(),a.getDni(),a.getFecha_nac(),a.getNacionalidad()});
        }
    }
    public void llenarBuscado(String nombre, String apellido){
        Autor a=ad.buscarAutor(nombre, apellido);
        modelo.addRow(new Object[]{a.getId_autor(),a.getNombreAutor(),a.getApellidoAutor(),a.getDni(),a.getFecha_nac(),a.getNacionalidad()});
    }
    public void borrarFilas(){
        int a=modelo.getRowCount()-1;
        
        for(int i=a;i>=0;i--){
            modelo.removeRow(i);
        }
        
    }
