    private void jbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbActualizarActionPerformed
        int filaElegida= jtAutores.getSelectedRow();
        if(filaElegida!=-1){
            Autor a=new Autor();
            a.setId_autor(Integer.parseInt(modelo.getValueAt(filaElegida, 0).toString()));
            a.setNombreAutor(modelo.getValueAt(filaElegida, 1).toString());
            a.setApellidoAutor(modelo.getValueAt(filaElegida, 2).toString());
            a.setDni(Integer.parseInt(modelo.getValueAt(filaElegida, 3).toString()) );
            LocalDate fecha= LocalDate.parse(modelo.getValueAt(filaElegida, 4).toString());
            a.setFecha_nac(fecha);
            a.setNacionalidad(modelo.getValueAt(filaElegida, 5).toString());
            ad.modificarAutor(a);
            
        }
    }//GEN-LAST:event_jbActualizarActionPerformed


